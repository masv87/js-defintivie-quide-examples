/**Float examples **/
/**
 * floating syntax is: [digits][.digits][(E|e)[(+|-)]digits]
 */
var e = 3.14;
var f = .14; //0.14
var g = 6.02e12; //6.02 * 10^12
var h = 6.02E12; //6.02 * 10^12
var i = 6.02E-12; //6.02 * 10^-12